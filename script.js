document.addEventListener('DOMContentLoaded', () => {

  // 1. Language Toggle
  const langToggleBtn = document.getElementById('langToggle');
  let currentLang = localStorage.getItem('sakhra_lang') || 'ar';
  if (currentLang !== 'ar' && currentLang !== 'he') {
    currentLang = 'ar';
  }

  function applyLanguage(lang) {
    // Both Arabic (ar) and Hebrew (he) are RTL languages
    document.body.classList.remove('ltr');
    document.body.dir = 'rtl';

    if (lang === 'he') {
      if (langToggleBtn) langToggleBtn.innerText = "AR";
    } else {
      if (langToggleBtn) langToggleBtn.innerText = "HE";
    }

    // Update texts
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (content[lang] && content[lang][key]) {
        if (el.tagName === 'INPUT' && el.type === 'button') {
          el.value = content[lang][key];
        } else {
          el.innerHTML = content[lang][key];
        }
      }
    });

    localStorage.setItem('sakhra_lang', lang);
  }

  // Init language
  applyLanguage(currentLang);

  if (langToggleBtn) {
    langToggleBtn.addEventListener('click', () => {
      currentLang = currentLang === 'ar' ? 'he' : 'ar';
      applyLanguage(currentLang);
    });
  }

  // 2. Sticky Navbar
  const navbar = document.querySelector('.navbar');
  window.addEventListener('scroll', () => {
    if (window.scrollY > 50) {
      navbar.classList.add('scrolled');
    } else {
      navbar.classList.remove('scrolled');
    }
  });

  // 3. Hamburger Menu
  const hamburger = document.getElementById('hamburger');
  const navLinks = document.getElementById('navLinks');
  if (hamburger && navLinks) {
    hamburger.addEventListener('click', () => {
      navLinks.classList.toggle('active');
    });
  }

  // 4. Counter Animation (Intersection Observer)
  const counters = document.querySelectorAll('.counter-num');
  const speed = 200;

  const animateCounters = (entries, observer) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        const counter = entry.target;
        const target = +counter.getAttribute('data-target');

        const updateCount = () => {
          const count = +counter.innerText;
          const inc = target / speed;

          if (count < target) {
            counter.innerText = Math.ceil(count + inc);
            setTimeout(updateCount, 15);
          } else {
            counter.innerText = target + (counter.hasAttribute('data-plus') ? "+" : "");
          }
        };

        updateCount();
        observer.unobserve(counter);
      }
    });
  };

  const counterObserver = new IntersectionObserver(animateCounters, {
    threshold: 0.5
  });

  counters.forEach(counter => {
    counterObserver.observe(counter);
  });

  // 5. Lightbox for Galleries
  const galleryItems = document.querySelectorAll('.gallery-item img');
  const lightbox = document.getElementById('lightbox');
  const lightboxImg = document.getElementById('lightbox-img');
  const lightboxClose = document.getElementById('lightbox-close');

  if (lightbox) {
    galleryItems.forEach(item => {
      item.addEventListener('click', () => {
        lightboxImg.src = item.src;
        lightbox.classList.add('active');
      });
    });

    lightboxClose.addEventListener('click', () => {
      lightbox.classList.remove('active');
    });

    lightbox.addEventListener('click', (e) => {
      if (e.target === lightbox) {
        lightbox.classList.remove('active');
      }
    });
  }

  // 6. Intersection Observer for FadeInUp Animation
  const animatedElements = document.querySelectorAll('.fade-in-up');
  const fadeObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        fadeObserver.unobserve(entry.target);
      }
    })
  }, { threshold: 0.1 });

  animatedElements.forEach(el => fadeObserver.observe(el));

  // 7. Gallery Category Filtering System
  const filterBtns = document.querySelectorAll('.filter-btn');
  const galleryGridItems = document.querySelectorAll('.gallery-item');

  if (filterBtns.length > 0 && galleryGridItems.length > 0) {
    filterBtns.forEach(btn => {
      btn.addEventListener('click', () => {
        // Remove active class from all buttons
        filterBtns.forEach(b => b.classList.remove('active'));
        // Add active class to clicked button
        btn.classList.add('active');

        const filterValue = btn.getAttribute('data-filter');

        galleryGridItems.forEach(item => {
          if (filterValue === 'all' || item.getAttribute('data-category') === filterValue) {
            item.style.display = 'block';
            setTimeout(() => {
              item.style.opacity = '1';
              item.style.transform = 'scale(1)';
            }, 10);
          } else {
            item.style.opacity = '0';
            item.style.transform = 'scale(0.8)';
            setTimeout(() => {
              item.style.display = 'none';
            }, 300);
          }
        });
      });
    });
  }
});
